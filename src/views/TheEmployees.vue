  <div>
    <div class="page-title flex-between-center">
      <div class="flex-center">
        <h3 class="right-margin-big">–°–ø–∏—Å–æ–∫ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–æ–≤</h3>

        <div class="flex-center">
          <button class="btn-transparent transparent btn-page-title blue-text text-darken-1 "
                  @click="updateEmployees"
          ><i class="material-icons middle-material-icons">autorenew</i>
          </button>
        </div>
      </div>

      <AddCardEmployees
        @add-employee="addEmployee"
        :employees="employees"
      />
    </div>

    <div>
      <TableEmployees
        v-if="employees"
        :employees="employees"
        @remove-employee="removeEmployee"
      />
    </div>
  </div>
</template>

<script>
import TableEmployees from '@/components/employees/EmployeesTable'
import AddCardEmployees from '@/components/employees/EmployeesAdd'
import { mapActions, mapGetters, mapMutations } from 'vuex'
import firebase from 'firebase/app'

export default {
  name: 'Employees',

  components: { TableEmployees, AddCardEmployees },

  computed: {
    ...mapGetters([
      'employees'
    ])
  },

  created () {
    this.SET_EMPLOYEES_FROM_LOCAL_STORAGE()
  },

  methods: {
    ...mapMutations([
      'SET_EMPLOYEES_FROM_LOCAL_STORAGE',
      'SET_EMPLOYEES_FROM_SERVER'
    ]),

    ...mapActions([
      'registerEmployees'
    ]),

    removeEmployee (id) {
      firebase.database().ref('/employees/' + id).remove()
        .then(() => {
          this.SET_EMPLOYEES_FROM_SERVER()
          console.log('–°–æ—Ç—Ä—É–¥–Ω–∏–∫ —É–¥–∞–ª—ë–Ω üóëÔ∏è')
        })
    },

    async addEmployee (employee) {
      const regData = {
        email: employee.email,
        password: employee.password
      }

      employee.id = await this.registerEmployees(regData)

      firebase.database().ref('/employees/' + employee.id).set(employee)
        .then(() => {
          this.SET_EMPLOYEES_FROM_SERVER()
          console.log('–°–æ—Ç—Ä—É–¥–Ω–∏–∫ –¥–æ–±–∞–≤–ª–µ–Ω ‚ûï')
        })
    },

    updateEmployees () {
      this.SET_EMPLOYEES_FROM_SERVER()
      // eslint-disable-next-line no-undef
      M.toast({ html: '–°–æ—Ç—Ä—É–¥–Ω–∏–∫–∏ –æ–±–Ω–æ–≤–ª–µ–Ω—ã' })
    }
  }
}
</script>
